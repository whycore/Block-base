// Helper to get wallet icon
export function getWalletIcon(connectorId: string, className = "w-6 h-6") {
  const id = connectorId.toLowerCase();
  if (id.includes("metamask") || id.includes("injected")) {
    return <MetaMaskIcon className={className} />;
  }
  if (id.includes("coinbase")) {
    return <CoinbaseWalletIcon className={className} />;
  }
  if (id.includes("farcaster")) {
    return <FarcasterIcon className={className} />;
  }
  return <InjectedWalletIcon className={className} />;
}

